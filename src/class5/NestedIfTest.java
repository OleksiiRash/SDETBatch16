package class5;

public class NestedIfTest {
    public static void main(String[] args) {

        int n = 8;
        int n2 = n+n;

        if (n2 == 11)
            System.out.println("n==10");
        else if (n2 > 11)
                System.out.println("n>10");
            else
                System.out.println("n<11");

    }
}
