package replit;

public class replit48 {
    public static void main(String[] args) {

        // Using while loops that print out even numbers from 1 to 13

        int i = 1;
        while (i <= 13) {
            if (i % 2 == 0) // or we can use i+=2
                System.out.print(i + " ");
            i++;
        }


    }
}
