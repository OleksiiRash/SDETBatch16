package class2;

public class PrimitiveVariables {
    public static void main(String[] args) {
        byte age = 127;
        short distance = 1000;
        int myInt = 100000;
        long distanceToTheMoon = 384400L;
        float distanceToTheMoonNew = 384.400f;
        double distanceToTheMoonCorrect = 384.400;
        boolean myBoolean = true;
        char myChar = '$';
        System.out.println(age);
        System.out.println(distance);
        System.out.println(myInt);
        System.out.println(distanceToTheMoon);
        System.out.println(distanceToTheMoonNew);
        System.out.println(distanceToTheMoonCorrect);
        System.out.println(myBoolean);
        System.out.println(myChar);
    }
}
