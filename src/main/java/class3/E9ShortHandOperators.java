package class3;

public class E9ShortHandOperators {
    public static void main(String[] args) {

        /*
        int number = 10;
        number = number + 20;
        System.out.println(number);
        */

        int number = 10;
        number+=20; // shortcode in Java need to Learn for me!!!
        System.out.println(number);

        // shortcodes in Java

        int number2 = 10;
        number2+=90; // 100
        number2-=50; // 100-50 => 50
        number2*=2; // 50*2 = 100
        number2/=10; // 100/10=> 10
        System.out.println(number2);

        int number3 = 100;
        // number = number + 1;
        number3++; // increase the value of number3 by one
        System.out.println(number3);


















    }
}
