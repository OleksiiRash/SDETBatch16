package class5;

public class E5LogicalOperators {
    public static void main(String[] args) {

        /*
        Logical Operators in Java:
        && = and;
        // - or;
        ! - not;

        Important: those operators can be used between the boolean (true or false) values;
         */

        boolean flag = true;
        System.out.println(!flag); // ! - not and converted to the false value






    }
}
