package class2;

public class IntAndStringChangingDemo {
    public static void main(String[] args) {

        int numberLine =81;
        numberLine = 82;
        System.out.println(numberLine);

        String president = "Trump";
        president = "Biden";
        System.out.println(president);

        String name = "Oleksii";
        name = "Oleksii is";
        int age = 33;
        System.out.println(name + " " + age);

    }
}
