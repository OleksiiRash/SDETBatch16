package class7;

public class E3WhileLoop {
    public static void main(String[] args) {

        int num = 1;
        while (num <= 5) {
            System.out.println(num);
            num++;
        }

        // another way to do this with for loop

        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }


    }
}
