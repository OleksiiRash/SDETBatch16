package class1;

public class VariablesDemo {
    public static void main(String[] args) {
        /*
        we are telling the computer to create a box or container in
        which we are storing the number 20;
        int - always should be with a lowercase
         */
        int age=20;
        System.out.println("age");
        /*
        Reserve a box of type "String" call it name and store "Oleksii" inside it
        String method should be always start with uppercase
         */
        String name="Oleksii";
        String city="London";
        String country="UK";

        System.out.println(name);
        System.out.println(city);
        System.out.println(country);

    }
}
